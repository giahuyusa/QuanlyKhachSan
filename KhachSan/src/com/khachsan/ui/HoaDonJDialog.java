    void fillTableChiTietPhieuDatPhongDa1(){
        int row = tblChiTietPhieuDatPhongDa.getSelectedRow();
        String maPhieuDatPhong = txtMaPhieuDatPhong.getText();
        String maPhong = (String) tblChiTietPhieuDatPhongDa.getValueAt(row, 0);

        double tienPhong = 0;
        String maPhieuDatPhongStore = "";
        String maKhachHangStore = "";
        String soDienThoaiStore = "";
        String maNhanVien = "";
        String maPhieuHoaDon = "";

        DefaultTableModel model = (DefaultTableModel) tblChiTietPhieuDatPhongDa1.getModel();
        model.setRowCount(0);

        try {
            List<Object[]> list = chiTietPhieuDatPhongDao.getChiTietPhieuDatPhongDa1(maPhong, maPhieuDatPhong);
            for (Object[] obj : list) {
                model.addRow(new Object[]{obj[0], obj[1], obj[2], obj[3], obj[4]});

                // Kiểm tra và xử lý chuỗi rỗng trước khi chuyển đổi
                if (obj[4] != null && !obj[4].toString().isEmpty()) {
                    tienPhong += Double.parseDouble(obj[4].toString());
                }

                maPhieuDatPhongStore = (obj[5] != null) ? obj[5].toString() : "";
                maKhachHangStore = (obj[6] != null) ? obj[6].toString() : "";
                soDienThoaiStore = (obj[7] != null) ? obj[7].toString() : "";
                maNhanVien = (obj[8] != null) ? obj[8].toString() : "";
                maPhieuHoaDon = (obj[9] != null) ? obj[9].toString() : "";
            }
            txtPhong.setText(maPhong);
            txtTienPhong.setText(String.valueOf(tienPhong));
            txtPhieuDatPhongStore.setText(maPhieuDatPhongStore);
            txtKhachHangStore.setText(maKhachHangStore);
            txtSoDienThoaiStore.setText(soDienThoaiStore);
            txtPhieuHoaDon.setText(maPhieuHoaDon);
            txtMaNhanVien.setText(maNhanVien);
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu: " + e.getMessage());
        }
    }

