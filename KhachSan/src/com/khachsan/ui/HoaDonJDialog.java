    void print() {
        // Tạo một PrinterJob mới
        PrinterJob job = PrinterJob.getPrinterJob();
        job.setJobName("Print Panel");

        // Đặt đối tượng Printable và triển khai phương thức print đơn giản hơn
        job.setPrintable((pg, pf, pageNum) -> {
            if (pageNum > 0) {
                return Printable.NO_SUCH_PAGE;
            }

            // In panel với hướng giấy ngang và thu nhỏ kích thước
            Graphics2D g2 = (Graphics2D) pg;
            g2.translate(pf.getImageableX(), pf.getImageableY());
            g2.scale(0.47, 0.47);
            panelToPrint.print(g2);

            return Printable.PAGE_EXISTS;
        });

        // Hiển thị hộp thoại in và thực hiện lệnh in nếu người dùng đồng ý
        if (job.printDialog()) {
            try {
                job.print();
            } catch (PrinterException ex) {
                ex.printStackTrace();
            }
        }
    }

    
